package com.herencia;

public class auto {

	String marca;
	
	int ruedas;
	public auto(String marca,int ruedas ){
		this.marca=marca;
		this.ruedas=ruedas;
		
	}
	
	

	public String getMarca() {
		return marca;
	}
	
	public void setMarca(String marca) {
		this.marca=marca;
	}
	public int getRuedas() {
		return ruedas;
	}
	
	public void setRuedas(int ruedas) {
		this.ruedas=ruedas;
	}
	
}
